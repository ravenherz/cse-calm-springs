package com.ravenherz.rhzwe.util.pluggable.basic;

public class LoaderInformation {
    private String pluginName;
    private String pluginLoaderVersion;

    public String getPluginName() {
        return pluginName;
    }

    public void setPluginName(String pluginName) {
        this.pluginName = pluginName;
    }

    public String getPluginLoaderVersion() {
        return pluginLoaderVersion;
    }

    public void setPluginLoaderVersion(String pluginLoaderVersion) {
        this.pluginLoaderVersion = pluginLoaderVersion;
    }
}
